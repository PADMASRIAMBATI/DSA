x = 3.14
y = -4
z = 5

result = round(x) #Round function
print(f"Round function : {result}")

result1 = abs(y) #Absulte function(distance away from 0 and whole number)
print(f"Absulte function: {result1}")

result2 = pow(5,2)#Power function
print(f"Power function: {result2}")

result3 = max(x,y,z)
print(f"Maximum value: {result3}")

result4 = min(x,y,z)
print(f"Minimum value: {result4}")
